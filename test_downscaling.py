    "    l8_scaled = l8.map(applyScaleFactors).select(['SR_B.'])\n",
    "    \n",
    "    # reproject MODIS and Landsat datasets to Namibia projection \n",
    "    modis_interpolated = modis_interpolated.filterDate(first_date, last_date).select(['red', 'nir'])\n",
    "    modis_reproj = modis_interpolated.map(reprojectModis)\n",
    "    l8_reproj = l8_scaled.map(reprojectLandsat)\n",
    "    \n",
    "    # clip image\n",
    "    modis_clipped = modis_reproj.map(clipToAOI(large_region))\n",
    "    l8_clipped_TL_TL = l8_reproj.map(clipToAOI(large_region_TL_TL))\n",
    "    l8_clipped_TL_TR = l8_reproj.map(clipToAOI(large_region_TL_TR))\n",
    "    l8_clipped_TL_BR = l8_reproj.map(clipToAOI(large_region_TL_BR))\n",
    "    l8_clipped_TL_BL = l8_reproj.map(clipToAOI(large_region_TL_BL))\n",
    "    \n",
    "    l8_clipped_TR_TL = l8_reproj.map(clipToAOI(large_region_TR_TL))\n",
    "    l8_clipped_TR_TR = l8_reproj.map(clipToAOI(large_region_TR_TR))\n",
    "    l8_clipped_TR_BR = l8_reproj.map(clipToAOI(large_region_TR_BR))\n",
    "    l8_clipped_TR_BL = l8_reproj.map(clipToAOI(large_region_TR_BL))\n",
    "    \n",
    "    l8_clipped_BR_TL = l8_reproj.map(clipToAOI(large_region_BR_TL))\n",
    "    l8_clipped_BR_TR = l8_reproj.map(clipToAOI(large_region_BR_TR))\n",
    "    l8_clipped_BR_BR = l8_reproj.map(clipToAOI(large_region_BR_BR))\n",
    "    l8_clipped_BR_BL = l8_reproj.map(clipToAOI(large_region_BR_BL))\n",
    "    \n",
    "    l8_clipped_BL_TL = l8_reproj.map(clipToAOI(large_region_BL_TL))\n",
    "    l8_clipped_BL_TR = l8_reproj.map(clipToAOI(large_region_BL_TR))\n",
    "    l8_clipped_BL_BR = l8_reproj.map(clipToAOI(large_region_BL_BR))\n",
    "    l8_clipped_BL_BL = l8_reproj.map(clipToAOI(large_region_BL_BL))\n",
    "    \n",
    "    # add NDVI as a band to the MODIS dataset\n",
    "    modis_ndvi = modis_clipped.map(addNDVI)\n",
    "    \n",
    "    \n",
